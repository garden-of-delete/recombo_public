int id_knot(char *idknot, char *idpoly, int nknots)
{
   for (int i = 0; i < nknots; i++)
   {
      if (!strcmp(idpoly, poly [i]))
      {
         strcpy(idknot, knot [i]);
         return i;
      }
   }
   strcpy(idknot, "unknown");
   return UNKNOWN;
}

void same_polys()
{
   for (int i = 0; i < NUM_KNOTS; i++)
   {
      bool same = false;
      for (int k = 0; k < NUM_KNOTS; k++)
      {
         if (k == i) continue;
         if (!strcmp(poly [i], poly [k]))
         {
            same = true;
            break;
         }
      }
      if (!same) continue;
      printf("%s: ", knot [i]);
      for (int j = 0; j < NUM_KNOTS; j++)
      {
         if (i == j) continue;
         if (!strcmp(poly [i], poly [j]))
            printf("%s ", knot [j]);
      }
      printf("\n");
   }
   exit(102);
}
